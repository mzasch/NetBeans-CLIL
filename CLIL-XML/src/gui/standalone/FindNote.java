    private void btnTrovaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrovaActionPerformed
        SimpleDateFormat dateformat = new SimpleDateFormat("dd/MM/yyyy");
        try {
            currentDate = dateformat.parse(txtData.getText());
            setNoteList();
        } catch (ParseException ex) {
            lbError.setText("Data non valida!");
            lbError.setVisible(true);
        }
    }//GEN-LAST:event_btnTrovaActionPerformed

    private void btnEliminaNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminaNotaActionPerformed
        int selectedNote = noteList.getSelectedIndex();
        nb.getNotes().removeIf(n -> n.equals(notesOfDate.get(selectedNote)));
        noteList.removeAll();
        setNoteList();
    }//GEN-LAST:event_btnEliminaNotaActionPerformed

    private void setNoteList() {
        SimpleDateFormat timeformat = new SimpleDateFormat("HH:mm:ss");
        notesOfDate = nb.GetNotesByDate(currentDate);
        lbError.setVisible(false);
        String[] noteItems = new String[notesOfDate.size()];
        int i=0;
        for (Note note : notesOfDate) {
            noteItems[i] = timeformat.format(note.getCreation());
            i++;
        }
        noteList.setListData(noteItems);
    }
    

