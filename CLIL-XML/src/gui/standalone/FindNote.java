    @Override
    public void valueChanged(ListSelectionEvent e) {
        if (e.getValueIsAdjusting()== false) {
            int selectedNote = noteList.getSelectedIndex();
            if (selectedNote == -1) {
                btnEliminaNota.setEnabled(false);
            } else {
                btnEliminaNota.setEnabled(true);
                selectedNoteText.setText(notesOfDate.get(selectedNote).getDescription());
            }
        }
    }

